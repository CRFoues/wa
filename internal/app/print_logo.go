func PrintLogo(more bool) {
	if is1024Day() {
		fmt.Print(logo1024)
	} else {
		fmt.Print(logo)
	}

	if more {
		fmt.Print(moreLogos)
	}
	fmt.Println()
}

func is1024Day() bool {
	_, month, day := time.Now().Date()
	return month == 10 && day == 24
}

